package com.kisho;

public class Helper {
	 
	   
		
		 int rollno;
	    
		 String name;
		public Helper(int rollno, String name) {
			this.rollno = rollno;
			this.name = name;
		}
		public int getRollno() {
			return rollno;
		}
		public void setRollno(int rollno) {
			this.rollno = rollno;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
}
